package com.bycc.dto.caseAlarm;

public class CaseAlarmDto {
	private String caseName;
	private String caseAlarmType;
	private String content;
	
	
	public String getCaseName() {
		return caseName;
	}
	public void setCaseName(String caseName) {
		this.caseName = caseName;
	}
	public String getCaseAlarmType() {
		return caseAlarmType;
	}
	public void setCaseAlarmType(String caseAlarmType) {
		this.caseAlarmType = caseAlarmType;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
}
